package br.ufal.ic.easy.refactoring.examples;

public class Product {
	
	protected String brandName;
	protected float price;
	
	
	
	public Product(String brandName, float price) {
		this.brandName = brandName;
		this.price = price;
	}


	public float getPrice() {
		return price;
	}
	
	
	
	
	
	
	
    
}
